#Faça um Programa que peça a temperatura em graus Celsius, transforme e mostre em graus Fahrenheit.

gf = float( input("Informe a temperatura em °C") )
gc = ((gf*(9/5))+32)

print("A temperatura do Graus °F em Graus °C é?", gc)

#Primeiro: A variavel GF serviu para a pessoa informar a temperatura em °Celsius
##Segundo: A variavel GC serviu para montar a formula de como se calcular a temperatura °F em Graus °C]
