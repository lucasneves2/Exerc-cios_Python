#Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

a = float( input("Qual a área do quadrado?") )
d = (a**2) * 2

print("O dobro da área do quadrado eh:", d)

#Primeiro: A variavel A serviu para a pessoa digitar a área do quadrado
#Segundo: A variavel D serviu para elevar a variavel A por 2 e multiplicar por 2
#Terceiro: A função PRINT serviu para imprimir o resultado da formula da variavel D