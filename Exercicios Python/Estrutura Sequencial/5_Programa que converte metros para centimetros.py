#Faça um Programa que converta metros para centímetros.

valor_em_metros = float(input('Digite o valor de uma medição feita em metros: '))

valor_em_centimetros = valor_em_metros * 100

print(" O valor convertido para centímetros é: ",valor_em_centimetros)

#Primeiro: A variavel VALOR_EM_METROS serviu para a pessoa digitar a sua medição feita em metros
#Segundo: A variavel VALOR_EM_CENTIMETROS serviu para mostrar que ela e igual a variavel VALOR_EM_METROS multiplicado por 100
#Terceiro: A função print serviu para imprimir o valor de metros convertido em centimetros  