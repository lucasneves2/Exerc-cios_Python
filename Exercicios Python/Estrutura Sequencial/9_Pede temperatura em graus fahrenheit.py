#Faça um Programa que peça a temperatura em graus Fahrenheit, transforme e mostre a temperatura em graus Celsius. C = 5 * ((F-32) / 9).

gc = float( input("Informe a temperatura em °F") )
gf = ((5 * (gc-32)) / 9)

print("A temperatura do Graus °C em Graus °F é?", gf)

#Primeiro: A variavel GC serviu para a pessoa informar a temperatura em °Fahrenheit
#Segundo: A variavel GF serviu para montar a formula de como se calcular a A temperatura do Graus °C em Graus °F
#Terceiro: A função PRINT serviu para imprimir o resultado da temperatura do Graus °C em Graus °F